func TestJobMonitor_ConcurrentAccess(t *testing.T) {
	m := newJobMonitor()

	var wg sync.WaitGroup
	for i := 0; i < 20; i++ {
		wg.Add(1)
		go func() {
			defer wg.Done()
			_ = m.GetJobs()
		}()
	}
	for i := 0; i < 10; i++ {
		wg.Add(1)
		go func(id int) {
			defer wg.Done()
			m.mu.Lock()
			m.jobs["job-concurrent"] = &monitoredJob{
				JobID: "job-concurrent",
				State: "running",
			}
			m.mu.Unlock()
		}(i)
	}
	wg.Wait()
}

