func writeTestStatus(t *testing.T, dir, state, progress string) {
	t.Helper()
	status := analysis.JobStatus{
		State:     state,
		Progress:  progress,
		StartedAt: time.Now(),
		UpdatedAt: time.Now(),
	}
	data, _ := json.MarshalIndent(status, "", "  ")
	if err := os.WriteFile(filepath.Join(dir, "status.json"), data, 0o644); err != nil {
		t.Fatal(err)
	}
}
